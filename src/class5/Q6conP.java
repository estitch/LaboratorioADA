package class5;

import java.util.Scanner;

public class Q6conP {

	public static void main(String[] args) {
		// la complejidad es de O(n^1/2)
		Scanner scan = new Scanner(System.in);
		System.out.println("ingrese un tamaño");
		int n = scan.nextInt();
		int p=0;
		for(int i=1;p<=n;i++)
			p=p+1;
	}

}
